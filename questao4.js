/**
 * 4. Dado os três lados de um triângulo determinar o perímetro do mesmo
 */

let perimetroA = Number(prompt("Inserir valor do lado A"))
let perimetroB = Number(prompt("Inserir valor do lado B"))
let perimetroC = Number(prompt("Inserir valor do lado C"))

let calculo = perimetroA + perimetroB + perimetroC
alert("Perimetro do triangulo: " + calculo)